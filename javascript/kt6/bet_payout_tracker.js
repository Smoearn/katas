  decodeMorse = function(morseCode){
    // remove first spaces.
    morseCode = morseCode.trim()

    morseCodeWords = morseCode.split(' ')
    let output = ''
    for (let i = 0; i < morseCodeWords.length; i++) {
        const element = morseCodeWords[i];
        if(MORSE_CODE[element]){
            output += MORSE_CODE[element]
        } if(morseCodeWords[i] == '' && morseCodeWords[i+1] == ''){
            output += ' '
        }
        
    }return output
  }





